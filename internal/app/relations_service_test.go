// TestIsAllowedRelationType covers the whitelist guard exposed for
// reuse by MCP and any future surface.
func TestIsAllowedRelationType(t *testing.T) {
	for _, ok := range AllowedRelationTypes {
		if !IsAllowedRelationType(ok) {
			t.Errorf("whitelist self-check failed for %q", ok)
		}
	}
	for _, bad := range []string{"", "RELATED", "unknown", "supersede"} {
		if IsAllowedRelationType(bad) {
			t.Errorf("invalid type %q should not be allowed", bad)
		}
	}
}
